var stripe_public_key = JSON.parse(document.getElementById('id_stripe-public-key').textContent);
var client_secret = JSON.parse(document.getElementById('id_client-secret').textContent);
var stripe = Stripe(stripe_public_key);
var elements = stripe.elements();

/* From stripe styles */
var style = {
    base: {
        color: '#000',
        fontFamily: '"Helvetica Neue", Helvetica, sans-serif',
        fontSmoothing: 'antialiased',
        fontSize: '16px',
        '::placeholder': {
            color: '#aab7c4'
        }
    },
    invalid: {
        color: '#dc3545',
        iconColor: '#dc3545'
    }
};
var card = elements.create('card', {style: style});
card.mount('#card-element');